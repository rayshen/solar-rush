import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';

const HORIZONS_URL = 'https://ssd.jpl.nasa.gov/api/horizons.api';
const HALLEY_COMMAND = '90000030';
const RANGE_START = '1800-01-01';
const RANGE_STOP = '2200-01-01';
const COARSE_STEP_DAYS = 16;
const FINE_STEP_DAYS = 2;
const FINE_RADIUS_AU = 6;
const POSITION_SCALE_AU = 2e-8;
const VELOCITY_SCALE_AU_PER_DAY = 2e-11;
const OUTPUT_PATH = fileURLToPath(new URL('../src/data/halleyHorizons.js', import.meta.url));

function parseHorizonsVectors(text) {
  const start = text.indexOf('$$SOE');
  const stop = text.indexOf('$$EOE');
  if (start < 0 || stop < 0) {
    throw new Error(`Horizons response did not contain vector data:\n${text.slice(0, 1200)}`);
  }

  return text.slice(start + 5, stop).trim().split('\n').filter(Boolean).map((line) => {
    const fields = line.split(',').map((field) => field.trim());
    return {
      jdTdb: Number(fields[0]),
      state: fields.slice(2, 8).map(Number),
    };
  });
}

async function fetchVectors(start, stop, stepDays) {
  const query = new URLSearchParams({
    format: 'text',
    COMMAND: HALLEY_COMMAND,
    EPHEM_TYPE: 'VECTORS',
    CENTER: '500@10',
    START_TIME: start,
    STOP_TIME: stop,
    STEP_SIZE: `${stepDays}d`,
    REF_PLANE: 'ECLIPTIC',
    REF_SYSTEM: 'ICRF',
    OUT_UNITS: 'AU-D',
    VEC_TABLE: '2',
    CSV_FORMAT: 'YES',
    VEC_CORR: 'NONE',
  });
  const response = await fetch(`${HORIZONS_URL}?${query}`);
  if (!response.ok) throw new Error(`Horizons request failed: ${response.status} ${response.statusText}`);
  return parseHorizonsVectors(await response.text());
}

function toIsoDate(jd) {
  return new Date((jd - 2_440_587.5) * 86_400_000).toISOString().slice(0, 10);
}

function findFineWindows(samples) {
  const active = samples.map(({ state }) => Math.hypot(state[0], state[1], state[2]) <= FINE_RADIUS_AU);
  const windows = [];
  for (let index = 0; index < samples.length; index += 1) {
    if (!active[index]) continue;
    const first = index;
    while (index + 1 < samples.length && active[index + 1]) index += 1;
    const last = index;
    windows.push({
      start: toIsoDate(samples[Math.max(0, first - 1)].jdTdb),
      stop: toIsoDate(samples[Math.min(samples.length - 1, last + 1)].jdTdb),
    });
  }
  return windows;
}

function encodeStates(samples) {
  const values = new Int32Array(samples.length * 6);
  samples.forEach(({ state }, sampleIndex) => {
    state.forEach((value, component) => {
      const scale = component < 3 ? POSITION_SCALE_AU : VELOCITY_SCALE_AU_PER_DAY;
      const quantized = Math.round(value / scale);
      if (quantized < -2_147_483_648 || quantized > 2_147_483_647) {
        throw new Error(`State component ${value} exceeds the Int32 range at scale ${scale}`);
      }
      values[sampleIndex * 6 + component] = quantized;
    });
  });
  return Buffer.from(values.buffer).toString('base64');
}

function serializeSegment(samples, stepDays) {
  return {
    startJdTdb: samples[0].jdTdb,
    stepDays,
    sampleCount: samples.length,
    statesBase64: encodeStates(samples),
  };
}

const coarse = await fetchVectors(RANGE_START, RANGE_STOP, COARSE_STEP_DAYS);
const fineWindows = findFineWindows(coarse);
const fineSegments = [];
for (const window of fineWindows) {
  fineSegments.push(serializeSegment(await fetchVectors(window.start, window.stop, FINE_STEP_DAYS), FINE_STEP_DAYS));
}

const payload = {
  source: 'NASA/JPL Horizons API',
  target: '1P/Halley (record 90000030, JPL solution 75)',
  center: 'Sun body center (500@10)',
  frame: 'Geometric heliocentric IAU76/J2000 ecliptic, ICRF, AU and AU/day',
  dynamicalModel: 'DE441 planets; SB441-N16 small perturbers; Marsden non-gravitational comet model',
  generatedAt: new Date().toISOString(),
  validStartJdTdb: coarse[0].jdTdb,
  validStopJdTdb: coarse.at(-1).jdTdb,
  fineRadiusAu: FINE_RADIUS_AU,
  positionScaleAu: POSITION_SCALE_AU,
  velocityScaleAuPerDay: VELOCITY_SCALE_AU_PER_DAY,
  coarse: serializeSegment(coarse, COARSE_STEP_DAYS),
  fine: fineSegments,
};

const source = `// Generated by scripts/fetch-halley-horizons.mjs. Do not edit by hand.\n` +
  `// Source: https://ssd.jpl.nasa.gov/horizons/\n` +
  `const halleyHorizonsEphemeris = ${JSON.stringify(payload, null, 2)};\n\n` +
  `export { halleyHorizonsEphemeris };\n`;

await writeFile(OUTPUT_PATH, source);
console.log(`Wrote ${OUTPUT_PATH}`);
console.log(`Coarse samples: ${coarse.length}; fine windows: ${fineSegments.length}; fine samples: ${fineSegments.reduce((sum, segment) => sum + segment.sampleCount, 0)}`);
